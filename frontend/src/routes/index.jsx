import { createBrowserRouter } from "react-router-dom";

import App from "../App.jsx";
import { Cart, Error, Home, Login, Register } from "../pages/index.js";

const router = createBrowserRouter([
  {
    path: "/",
    element: <App />,
    children: [
      {
        path: "/",
        element: <Home />,
      },
      {
        path: "*",
        element: <Error />,
      },
      {
        path: "/login",
        element: <Login />,
      },
      {
        path: "/register",
        element: <Register />,
      },
      {
        path: "/cart",
        element: <Cart />,
      },
      
    ],
  },
]);

export default router;
